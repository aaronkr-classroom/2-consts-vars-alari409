/*100만원이 있다. 이 돈을 연평균 10%의 수익률을 자랑하는 주식 시장에 투자하려고 한다.
(연간 12번의 복리 계산). 만약 40년동안  이 돈을 주식시장에 그대로 두면, 얼마가 될까? calculator.h  파일과
아래의 공식을 사용하여 계산하는 c프로그램을 작성하라. 지수 계산을 위해서는 math.h 라이브러리의 
pow(value, exponent) 함수를 사용하라

A = P(1 + r/n) ^ (nt)

여기서 
A = 구하고자 하는 값
P = 원금
r = 연이자율 (소수로 표현)
n = 복리 회수 (12번)
t = 기간(40년)

*/
#include "calculator.h"
#include <math.h>
#include "calculator.h"

int main()
{
	
	double P = 1000000;
	double r = 0.10; //10percent
	double n = 12; 
	double t = 40; 
	double A = P * pow((1 + r / n), (n * t));

	// double B = Mul(P, pow(Sum(1, Div(r, n)), Mul(n, t)));

	printf("From %.2fKRW => %.2fKRW!~\n", P, A);




	return 0;
}
